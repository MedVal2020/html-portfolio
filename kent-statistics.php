<p>All of Kent and Medway is currently under Tier 4 (stay at home) restrictions.<br>

The figures we're reporting now begin to show the effect of the national <br>
restrictions and those high figures from before lockdown are now starting to level<br> off.
We are encouraged that the majority of people are doing the right thing by<br>
 minimising their social interactions at home and carefully following social<br>
  distancing guidelines when they are out, but it’s really important that we protect<br>
   the most at risk people among our older residents and this needs to happen now if<br>
    we want to see restrictions eased further.<br>

The government’s decisions on tiers have been made by ministers based on<br>
 recommendations by government scientists and medics, taking into account:<br><br>

 • Infection rates in all age groups and in particular among the over 60s<br>
 • How quickly case rates are rising or falling<br>
 • The number of cases per 100,000 in the general population<br>
 • Pressure on the NHS – including current and projected NHS capacity <br>
   (admissions, bed occupancy and staff absences)<br>
 • Local factors and exceptional circumstances such as a local but contained<br> outbreak.<br>
<br>Please follow the rules that apply to Tier 4 and help the whole of Kent move out of <br>these very high
restrictions as soon as possible.</p><br>

