 <h3>Order The Home Test Kit </h3>
<hr class="new4">

<h4>  Before you start</h4>
<p>•	Read these instructions before you open the test kit
<br>•	Wash your hands for 20 seconds before you handle the test kit
<br>•	If more than one person in your home is taking the COVID-19 test, please make sure you have a test kit for each person.<br>
You will need to register each person’s test kit.
</p>
<p>Checklist: what you need to do
It’s very important that you follow these steps in the correct order.
<br>1.	Find your nearest Royal Mail priority postbox (5 to 10 mins). Do this before you take the swab test
<br>2.	Register test kit online (10 to 15 minutes). You must register to get your test results
<br>3.	Take swab sample (5 to 10 minutes)
<br>4.	Post your completed test kit. You must do this within 2 days of receiving your test kit, if possible
5.	Get test results by text and email (2 days)
</p>

<h4>Who can take the test</h4>
<p>
The test is suitable for the following people:<br>
•	Adults aged 18 and over: self-test (unless unable to do so)<br>
•	Teenagers aged 12 to 17: self-test with adult supervision<br>
•	Children 11 and under: adult to test (see instructions on how to test a child)
</p>

<h4>Coronavirus guidance and help</h4>
<p>
If you have coronavirus (COVID-19) symptoms or have contracted coronavirus (COVID-19), please refer to NHS COVID-19 guidance.
<br>If you have symptoms of coronavirus (COVID-19) and your condition gets worse, or you do not get better after 7 days, use the NHS 111 online coronavirus service. If you do not have internet access, call NHS 111. For a medical emergency dial 999.
</p>

<h4>1. Find your nearest priority postbox</h4>
<p>
Before you take your sample or register your kit, check collection times at your nearest Royal Mail priority postbox
You need to post your completed test kit no later than 1 hour before last collection.
Weekend collection times may differ from weekdays. Do not take your test on a bank holiday, take it on the following day instead, so it can be collected on time.
</p>

<p>
  To check collection times at your nearest Royal Mail priority postbox:
1.	Go to Royal Mail: services near you
2.	Click the ‘Show my nearest priority postboxes’ tickbox
3.	Enter the postcode of where you’re staying and select the address
4.	View priority postboxes near you and check the collection times
</p>

<h4>If you’re unable to get to a priority postbox</h4>
<p>
  If you’re unable to get to a priority postbox without using public transport, or unable to follow the government’s social distancing guidelines,
  are shielding, classed as vulnerable, or too unwell to leave your home, call us for guidance on when to take your test and how to book a courier collection:
<br>•	England, Wales and Northern Ireland: call 119 (free from mobiles and landlines)
<br>•	Scotland: call 0300 303 2713 (charged at your standard network rate). Lines are open every day, 7am to 11pm
</p>

<h4> 2. Register your test kit online</h4>
<p>Only register your kit just before you take your test.<br>
If you’re testing more than one person in your household, you must register each person’s kit online.
Anyone who does not register will not receive their test results.
</p>

<h5 style="text-decoration:underline;">Register your test kit online</h5>
<p>You’ll need:<br>
•	your 10-digit order ID (see your home test order confirmation email)<br>
•	your 11-character test kit barcode. Check this barcode is the same on your plastic tube, biohazard bag, and return box<br>
•	the 13-character Royal Mail barcode on the prepaid return label. Enter numbers and letters only and not the # hashtag symbol
</p>
<h4>3. Take swab sample</h4>
<h4>When to take your swab sample</h4>
<p>
  You can take your swab sample after you’ve checked the collection times of your nearest priority postbox and registered your test kit online.
Take the swab sample on the same day you’ll post it.
</p>
<br><h5>Tips for taking your test</h5>
<p>Taking the test will feel a little uncomfortable and unusual for most people.
Remain calm and confident as you go through the process. If possible, do a mock practice without using any of the testing materials.
</p>
