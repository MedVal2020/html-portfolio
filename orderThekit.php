<br><h5>  Take the sample</h5>
<p>  1.	Follow the instructions until you have checked the test kit contents.<br>
2.	Explain what you’re going to do before you swab the tonsils and nose.<br>
3.	Ask the child to blow their nose and cough into a tissue. This is so that excess mucus does
 not interfere with the test. Throw the tissue away in a closed bin.<br>
4.	Look inside the child’s mouth and find their tonsils at the back of the throat.
 You may use the torch on your phone to help you. This is where you’ll swab the sample.
 If you cannot swab their tonsils, you can swab both of their nostrils instead.
 The result may be less accurate than a nose and tonsil swab.<br>
5.	Hold the swab in your hand, ask the child to open their mouth wide and
tilt their head back. Gently rub the swab over both tonsils and the back of
the throat for 10 seconds (use the torch on your phone to help you to see).
 Remove the swab from the back of the throat.<br>
6.	Put the same end of the same swab gently into one of their nostrils until you feel
some resistance. Rotate the swab for 10 to 15 seconds and slowly remove it.
If you did not swab the tonsils, swab both nostrils for 10 to 15 seconds each.<br>
7.	Put the swab into the vial with the fabric tip facing down. Snap off the stick end,
so that it fits inside without bending. Securely screw the lid onto the vial. The liquid must stay in the vial.<br>
8.	Go back to step 11 for how to package the test kit.


</p><br>

