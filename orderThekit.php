<h4>How to test someone else ?</h4>

<p>If you’re <u>testing a child</u>, see the instructions at the end of this document.</p>

<p>•	Talk through the steps together. If possible, do a mock practice without using any of the testing materials.
<br>•	Remain calm and confident as you go through the process. This will help the person to stay calm too.
</p>
<h4>Take the sample</h4>
<p>•	Talk them through what you’re going to do for both the tonsils and nose sample.<br>
•	Get them to say ‘ahhhhhhhhhh’ loudly for 5 seconds, so that you can have a view of their tonsils at the back
of their throat.
<br>•	Get them to say ‘ahhh’ loudly 3 times, then introduce the swab stick and ask them to keep saying ‘ahhh’,
 while you take the swab sample.<br>

<h4>How to take your swab sample</h4>
<h5>Step 1</h5>
<p>Wash your hands thoroughly for 20 seconds using soap and warm water before handling
the test kit to avoid contaminating it.</p>

<h5>Step 2</h5>
<p>Clean and dry a surface and then place the home test kit items on it.</p>

<h5>Step 3</h5>
<p>Use the test kit checklist to make sure you have everything. Test kit contents:•	swab, inside sealed wrapper
•	plastic tube, containing a small amount of liquid. The liquid must stay in the vial<br>
•	white absorbent pad<br>
•	zip-lock bag<br>
•	biohazard bag with silver seal<br>
•	return box with pre-paid Royal Mail return label<br>
•	security seal, for the return box
</p>
<p><strong>If you notice anything damaged, broken, missing or difficult to use in the kit, do not use it</strong></p>

<p>Call your local number and request a new kit if something is broken,
  damaged, missing or difficult to use:&nbsp;111</p>

  <h5>Step 4</h5>
  <p>Look inside the mouth and find the tonsils at the back of the throat. Tonsils or where they would have been (if they are removed) are where you’ll swab the sample.
If you’re performing this on yourself use a mirror to help. If you are doing this for someone else, use a torch to help.
  </p>


  <h5>Step 5</h5>
  <p>Gently blow your/their nose and cough into a tissue. This is so that excess mucus does not interfere with the test.
Throw the tissue away in a closed bin.
  </p>

  <h5>Step 6</h5>
  <p>Wash your hands thoroughly for 20 seconds using soap and warm water before
     handling the test kit to avoid contaminating it.
  </p>

  <h5>Step 7</h5>
  <p>Open the package and gently take out the swab. This will be used for both tonsils and nose.
<strong>Do not touch your tongue, teeth, cheeks, gums, or any other surfaces with the fabric tip of the swab.
If it touches anything else, it may spoil your sample.</strong>
Taking the sample may cause gagging and minor discomfort, but it should not hurt.
  </p>

  <h5>Step 8</h5>
  <p>Holding the swab in your hand, open the mouth wide and gently rub the swab’s fabric tip over both tonsils at the back of the throat for 10 seconds (use a torch and/or mirror to help you do this).
Remove the swab from the back of the throat.
  </p>

  <h5>Step 9</h5>
  <p>Put the same end of the same swab gently into the nostril about 2.5cm (1 inch) or until you feel some resistance. Rotate the swab for 10 to 15 seconds and slowly remove it.
You only need to sample one nostril. If you have a piercing use the other nostril.<br>
<strong>This may feel uncomfortable. Do not insert the swab any deeper if you or they feel strong resistance or pain.</strong>

  </p>


<h5>  Step 10</h5>
<p>  Put the swab into the vial with the fabric tip facing down. Snap off the stick end, so that it fits inside without bending.
  Securely screw the lip onto the vial so that no liquid can leak.<br>
<strong>  You must keep the liquid in the vial. You may need to apply pressure to get the lid on.</strong></p>

<h5>  Step 11</h5>
<p>  Wash your hands thoroughly for 20 seconds using soap and warm water.</p>

<h5>  Step 12</h5>
<p>  1.	First place the absorbent pad into the zip-lock bag<br>
  2.	Then place the vial next to the absorbent pad in the same bag<br>
  3.	Seal the bag</p>

<h5>  Step 13</h5>
<p>  Place the zip-lock bag into the biohazard bag. Make sure the barcodes
  on the plastic vial and the biohazard bag match. Seal the bag using its silver seal.</p>

<h5>  Step 14</h5>
<p>  1.	Assemble the return box, following the instructions on the box. If you can,
   wipe down the bag and box with an antiseptic wipe<br>
  2.	Put the biohazard bag into the box and close the box<br>
  3.	Apply the security seal as directed</p>

  <h5>  Step 15</h5>
  <p>  Take a picture or note of the barcode on your Royal Mail return label,
    so you can track the delivery. You’re now ready to post your test kit.
    <br> <strong> If you’ve been harmed or had a reaction while using this
      kit </strong> report it on the <u> Coronavirus Yellow Card reporting site</u></p>


      <h5>  Post completed test kit</h5>
      <p>  Post your completed test kit no later than 1 hour before the last collection time of the day (weekend times may differ from weekdays). Do not take your test on a bank holiday. Take it on the following day instead.
You can walk, cycle or drive to your nearest priority postbox to post your test kit. You must follow the government’s social distancing guidelines, wear a mask and must not take public transport.
Only use a Royal Mail priority postbox with the label and one of the NHS regional logos. You must not enter any Post Office with your test kits.
Some priority postboxes have a Sunday collection.
</p>

<h5>  Get test results by text and email</h5>
<p>  You should get your results 2 days after your home test is collected. It may take longer due to the volume of tests being processed.
Your results will be sent by email and as a text to the mobile number you provided when you registered online.
If you’ve not received your results after 5 days
Please call us if you’ve not received your results after 5 days:<br>
• call 119 (free from mobiles and landlines)
</p>

<h5>  How to test a child</h5>
<p>  •	Do not conduct this test if you do not feel confident<br>
•	Do not continue the test if the child has any pain or asks to stop<br>
•	If for any reason you cannot do the test, please continue to self-isolate as a household<br>
•	The fabric tip of the swab must not touch anything apart from the tonsils at the
back of throat and nose. If it touches anything else, it may spoil your sample

</p>

<h5> Prepare to take the sample</h5>
<p>  Remain calm and confident as you go through the process. This will help the child to stay calm too.
Talk through the steps together. If possible, practise without using any of the testing materials.
If the child is old enough, explaining the test in terms they can understand might help ease any fear. For example, “You may want to push the swab away, but it is really important you let me tickle your throat and nose so that we can tell if you might have coronavirus”.
For younger children, it may be helpful to give them a distraction while you conduct the test (such as a video) or make it into a game. You could also plan a reward for the child after the sample is taken.
Some children may feel more comfortable sitting on a caregiver’s lap or have someone to hold their hand to try making them feel more comfortable and secure. Decide before starting who will comfort the child and who will do the test.
Play games and suggest they stick their tongue out (get them to copy you), then get them to say ‘ahhhhhhhhhh’. Show them the swab stick and have them keep saying ‘ahhhh’ while you swab their tonsils.
Your child may have some gagging or brief discomfort when the swab touches their tonsils. This is normal for all age groups.
</p>

