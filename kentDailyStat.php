<h2>Coronavirus (COVID-19)</h2><br>
<p>Covid-19 is a virus from the coronavirus family, which are a group of viruses that vary in severity
  from the common cold to Severe Acute Respiratory Syndrome (SARS).
The KPHO is tracking the spread of the covid-19 epidemic within Kent and neighboring areas.
 We are monitoring coronavirus cases and deaths that occur in Kent on a daily basis.</p><br>
 <h3><span style="color:rgb(100, 179, 154);font-weight:bold">Data and reports</span></h3>

 <p>We have produced an excel dashboard that is used to monitor positive covid-19 tests from
   hospital (pillar 1) and third party (pillar 2) testing sites for Kent and Medway residents.
   <br>Please note some graphs may not display correctly on older versions of excel.</p><br>

